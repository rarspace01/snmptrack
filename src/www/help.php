<div id="seitehilfe">	
	<h1>Fragen zu TiNts?</h1>
	<h2>Hier finden Sie Hilfe zu vielen Funktionen und FAQs</h2>	
		<ul>
		<li> <b>Registrierung</b><br>
			Sie sind noch kein Nutzer bei TiNts?<br>
			Auf der Startseite koennen Sie dies nachholen!<br>
			Klicken Sie dazu auf den Link "Registrieren" der Login-Box.Sobald Sie im nächsten Fenster Ihre Kontaktdaten eingegeben haben und "Registrieren" klicken, bekommen Sie eine Bestätigungsmail mit dem Aktivierungslink.<br>
		<li> <b>Tickets suchen/anzeigen</b> </li>
			Um ihr gewünschtes Ticket zu finden, können Sie die Suche in der Navigationsleiste benutzen.
			Alternativ bekommen Sie unter dem Button "Alle Tickets" der Übersichtsektion eine Tabelle der bestehenden Tickets angezeigt.<br>
		<li> <b>RSS-Feed und AddThis </b></li>
			TiNts kann per RSS-Feed aboniert bzw. mit AddThis Anderen empfohlen werden. Um den Feed zu abonieren klicken sie in Ihrer Navigationsleiste den RSS-Button.
			In der Login-Box finden Sie das Funktionselement "Share", welches Weiterleitungsmöglichkeiten zu de gängigsten Social-Network seiten bietet.<br>
		<li> <b>Ticket verfassen </b></li>
			Um ein Ticket zu verfassen, haben Sie mehrer Möglichkeiten.
			Zum einen können Sie dies über den Button "Neues Ticket" der Navigationsleiste.
			Zum anderen unter dem Punkt "Übersicht" durch Klick auf den "Neues Ticket"-Button<br>
		<li> <b>Kommentare verfassen </b></li>
			Zu jedem Ticket können Kommentare verfasst werden. Hierzu finden Sie am Ende jedes Tickets eine Textbox, welche mit Ihrem persönlichen Kommentar gefüllt werden kann. Mit Klick auf "Hinzufügen" wird das Kommentar gespeichert.
		<li> <b>Profilfunktionen </b></li>
			Nach Registrierung können Sie jederzeit Ihre Profildaten aktualisieren. Ihr Profil finden Sie unter dem Navimenüpunkt "Profil" oder in den Übersichtsfunktionen. Dort können Daten wie Avatarbild, Anzeigename oder Passwort geändert werden.
		<li> <b>Passwort ändern </b></li>
			Um Ihr persönliches Passowrt zu ändern, gehen Sie zum Menüpunkt "Profil". Dort kann ein neues Passwort in das Textfeld "Passwort" eingegeben werden. Um es zu speichern, klicken Sie auf aktualisieren.
		<li> <b>Konto kündigen</b></li>
			Wenn Sie Ihren TiNts-Account entgültig kündigen wollen, können Sie dies in Ihrem Profil unter dem Button "Benutzerkonto kündigen" durchführen.<br>
			!Achtung! Ein gelöschter Account kann nicht wiederhergestellt werden.
		
		</ul>
